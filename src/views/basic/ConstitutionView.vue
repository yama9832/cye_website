    <section class="constitution-section" id="chapter-3">
      <h2 class="section-title">第三章 臣民の権利及び義務</h2>

      <div class="article">
        <h3 class="article-title">第十条【臣民の要件】</h3>
        <p class="article-text">やんまー帝国臣民たる要件は、法律でこれを定める。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第十一条【基本的人権の特例】</h3>
        <p class="article-text">臣民は、すべての基本的人権を享有するが、これは国王の統治及び公共の福祉を妨げない範囲に限られる。この憲法が臣民に保障する基本的人権は、侵すことのできない永久の権利として、国王より信託されたものである。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第十二条【自由・権利の保持の責任と濫用の禁止】</h3>
        <p class="article-text">この憲法が臣民に保障する自由及び権利は、臣民の不断の努力によって、国王への奉仕のために保持されなければならない。また、臣民は、これを濫用してはならず、常に公共の福祉及び帝国の発展のためにこれを利用する責任を負う。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第十三条【個人の尊重・幸福追求権】</h3>
        <p class="article-text">すべて臣民は、個人として尊重される。生命、自由及び幸福追求に対する臣民の権利については、国王の威光に反しない限り、立法その他の国政の上で、最大の尊重を必要とする。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第十四条【法の下の平等】</h3>
        <p class="article-text">すべて臣民は、法の下に平等であって、人種、信条、性別、社会的身分又は門地により、政治的、経済的又は社会的関係において、差別されない。ただし、帝国への貢献度による区別はこれを妨げない。</p>
        <p class="article-text">2 華族その他の貴族の制度は、これを認めない。</p>
        <p class="article-text">3 栄誉、勲章その他の栄典の授与は、いかなる特権も伴わない。栄典の授与は、現にこれを有し、又は将来これを受ける者の一代に限り、その効力を有する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第十五条【公務員の選定罷免権】</h3>
        <p class="article-text">公務員を選定し、及びこれを罷免することは、国王の権限に属する。</p>
        <p class="article-text">2 すべて公務員は、全体の奉仕者であって、一部の奉仕者ではない。</p>
        <p class="article-text">3 公務員の選挙については、成年者による普通選挙を保障する。</p>
        <p class="article-text">4 すべて選挙における投票の秘密は、これを侵してはならない。選挙人は、その選択に関し公的にも私的にも責任を問われない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第十六条【請願権】</h3>
        <p class="article-text">何人も、損害の救済、公務員の罷免、法律、命令又は規則の制定、廃止又は改正その他の事項に関し、平穏に請願する権利を有し、何人も、かかる請願をしたために、いかなる差別待遇も受けない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第十七条【国及び公共団体の賠償責任】</h3>
        <p class="article-text">何人も、公務員の不法行為により、損害を受けたときは、法律の定めるところにより、国又は公共団体に、その賠償を求めることができる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第十八条【奴隷的拘束及び苦役からの自由】</h3>
        <p class="article-text">何人も、いかなる奴隷的拘束も受けない。また、犯罪に因る処罰の場合を除いては、その意に反する苦役に服させられない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第十九条【思想及び良心の自由】</h3>
        <p class="article-text">思想及び良心の自由は、これを侵してはならない。ただし、帝国の秩序を乱す思想はこの限りではない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十条【信教の自由】</h3>
        <p class="article-text">信教の自由は、何人に対してもこれを保障する。いかなる宗教団体も、国から特権を受け、又は政治上の権力を行使してはならない。</p>
        <p class="article-text">2 何人も、宗教上の行為、祝典、儀式又は行事に参加することを強制されない。</p>
        <p class="article-text">3 国及びその機関は、宗教教育その他いかなる宗教的活動もしてはならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十一条【集会・結社・表現の自由と通信の秘密】</h3>
        <p class="article-text">集会、結社及び言論、出版その他一切の表現の自由は、これを保障する。</p>
        <p class="article-text">2 検閲は、これを行わない。通信の秘密は、これを侵してはならない。ただし、公安及び秩序維持のために必要な合理的措置は検閲とはみなされない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十二条【居住・移転・職業選択の自由】</h3>
        <p class="article-text">何人も、公共の福祉に反しない限り、居住、移転及び職業選択の自由を有する。</p>
        <p class="article-text">2 何人も、外国に移住し、又は国籍を離脱する自由を侵されない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十三条【学問の自由】</h3>
        <p class="article-text">学問の自由は、これを保障する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十四条【家族生活】</h3>
        <p class="article-text">婚姻は、両性の合意のみに基づいて成立し、夫婦が同等の権利を有することを基本として、相互の協力により、維持されなければならない。</p>
        <p class="article-text">2 配偶者の選択、財産権、相続、住居の選定、離婚並びに婚姻及び家族に関するその他の事項に関しては、法律は、個人の尊厳と両性の本質的平等に立脚して、制定されなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十五条【生存権】</h3>
        <p class="article-text">すべて臣民は、健康で文化的な最低限度の生活を営む権利を有する。</p>
        <p class="article-text">2 国は、すべての生活部面について、社会福祉、社会保障及び公衆衛生の向上及び増進に努めなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十六条【教育を受ける権利】</h3>
        <p class="article-text">すべて臣民は、法律の定めるところにより、その能力に応じて、ひとしく教育を受ける権利を有する。</p>
        <p class="article-text">2 すべて臣民は、法律の定めるところにより、その保護する子女に普通教育を受けさせる義務を負う。義務教育は、これを無償とする。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十七条【勤労の権利及び義務】</h3>
        <p class="article-text">すべて臣民は、勤労の権利を有し、義務を負う。勤労は国王への奉仕である。</p>
        <p class="article-text">2 賃金、就業時間、休息その他の勤労条件に関する基準は、法律でこれを定める。</p>
        <p class="article-text">3 児童は、これを酷使してはならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十八条【労働基本権】</h3>
        <p class="article-text">勤労者の団結する権利及び団体交渉その他の団体行動をする権利は、これを保障する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二十九条【財産権】</h3>
        <p class="article-text">財産権は、これを侵してはならない。</p>
        <p class="article-text">2 財産権の内容は、公共の福祉に適合するように、法律でこれを定める。</p>
        <p class="article-text">3 私有財産は、正当な補償の下に、これを公共のために用いることができる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十条【納税の義務】</h3>
        <p class="article-text">臣民は、法律の定めるところにより、納税の義務を負う。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十一条【適正手続の保障】</h3>
        <p class="article-text">何人も、法律の定める手続によらなければ、その生命若しくは自由を奪われ、又はその他の刑罰を科せられない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十二条【裁判を受ける権利】</h3>
        <p class="article-text">何人も、裁判所において裁判を受ける権利を奪われない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十三条【逮捕の要件】</h3>
        <p class="article-text">何人も、現行犯として逮捕される場合を除いては、権限を有する司法官憲が発し、かつ理由となっている犯罪を明示した令状によらなければ、逮捕されない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十四条【抑留・拘禁の要件】</h3>
        <p class="article-text">何人も、理由を直ちに告げられ、かつ、直ちに弁護人に依頼する権利を与えられなければ、抑留又は拘禁されない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十五条【住居侵入・捜索・押収】</h3>
        <p class="article-text">何人も、その住居、書類及び所持品について、侵入、捜索及び押収を受けることのない権利は、第三十三条の場合を除いては、正当な理由に基づいて発せられ、かつ捜索する場所及び押収する物を明示した令状がなければ、侵されない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十六条【拷問及び残虐な刑の禁止】</h3>
        <p class="article-text">公務員による拷問及び残虐な刑罰は、絶対にこれを禁ずる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十七条【刑事被告人の権利】</h3>
        <p class="article-text">すべて刑事事件においては、被告人は、公平な裁判所の迅速な公開裁判を受ける権利を有する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十八条【自己負罪拒否・自白】</h3>
        <p class="article-text">何人も、自己に不利益な供述を強要されない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三十九条【遡及処罰の禁止・一事不再理】</h3>
        <p class="article-text">何人も、実行の時に適法であった行為又は既に無罪とされた行為については、刑事上の責任を問われない。また、同一の犯罪について、重ねて刑事上の責任を問われない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四十条【刑事補償】</h3>
        <p class="article-text">何人も、抑留又は拘禁された後、無罪の裁判を受けたときは、法律の定めるところにより、国にその補償を求めることができる。</p>
      </div>
    </section>

    <section class="constitution-section" id="chapter-4">
      <h2 class="section-title">第四章 帝国議会</h2>

      <div class="article">
        <h3 class="article-title">第四十一条【議会の地位】</h3>
        <p class="article-text">帝国議会は、国王の諮問機関であり、国の唯一の立法機関である。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四十二条【議会の構成】</h3>
        <p class="article-text">帝国議会は、上院及び下院の両議院でこれを構成する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四十三条【両議院の組織】</h3>
        <p class="article-text">両議院は、全国民を代表する選挙された議員でこれを組織する。</p>
        <p class="article-text">2 両議院の議員の定数は、法律でこれを定める。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四十四条【議員及び選挙人の資格】</h3>
        <p class="article-text">両議院の議員及びその選挙人の資格は、法律でこれを定める。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四十五条【上院議員の任期】</h3>
        <p class="article-text">上院議員の任期は、四年とする。ただし、上院解散の場合には、その期間満了前に終了する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四十六条【下院議員の任期】</h3>
        <p class="article-text">下院議員の任期は、六年とし、三年ごとに議員の半数を改選する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四十七条【選挙区等】</h3>
        <p class="article-text">選挙区、投票の方法その他両議院の議員の選挙に関する事項は、法律でこれを定める。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四十八条【両議院兼職の禁止】</h3>
        <p class="article-text">何人も、同時に両議院の議員となることはできない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四十九条【歳費】</h3>
        <p class="article-text">両議院の議員は、法律の定めるところにより、国庫から相当額の歳費を受ける。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十条【不逮捕特権】</h3>
        <p class="article-text">両議院の議員は、法律の定める場合を除いては、国会の会期中逮捕されず、会期前に逮捕された議員は、その議院の要求があれば、会期中これを釈放しなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十一条【発言・表決の免責】</h3>
        <p class="article-text">両議院の議員は、議院で行った演説、討論又は表決について、院外で責任を問われない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十二条【常会】</h3>
        <p class="article-text">国会の常会は、毎年一回これを召集する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十三条【臨時会】</h3>
        <p class="article-text">内閣は、国会の臨時会の召集を決定することができる。いずれかの議院の総議員の四分の一以上の要求があれば、内閣は、その召集を決定しなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十四条【解散・総選挙・特別会】</h3>
        <p class="article-text">上院が解散されたときは、解散の日から四十日以内に、上院議員の総選挙を行い、その選挙の日から三十日以内に、国会を召集しなければならない。</p>
        <p class="article-text">2 上院が解散されたときは、下院は、同時に閉会となる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十五条【資格争訟裁判】</h3>
        <p class="article-text">両議院は、各々その議員の資格に関する争訟を裁判する。ただし、議員の議席を失わせるには、出席議員の三分の二以上の多数による議決を必要とする。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十六条【定足数・表決】</h3>
        <p class="article-text">両議院は、各々その総議員の三分の一以上の出席がなければ、議事を開き議決することができない。</p>
        <p class="article-text">2 両議院の議事は、この憲法に特別の定のある場合を除いては、出席議員の過半数でこれを決し、可否同数のときは、議長の決するところによる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十七条【会議の公開】</h3>
        <p class="article-text">両議院の会議は、公開とする。ただし、出席議員の三分の二以上の多数で議決したときは、秘密会を開くことができる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十八条【役員選任・規則制定権・懲罰】</h3>
        <p class="article-text">両議院は、各々その議長その他の役員を選任する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五十九条【法律案の議決】</h3>
        <p class="article-text">法律案は、この憲法に特別の定のある場合を除いては、両議院で可決し、国王の署名を経たとき法律となる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六十条【予算の議決】</h3>
        <p class="article-text">予算は、さきに上院に提出しなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六十一条【条約の承認】</h3>
        <p class="article-text">条約の締結に必要な国会の承認については、前条第二項の規定を準用する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六十二条【国政調査権】</h3>
        <p class="article-text">両議院は、各々国政に関する調査を行い、これに関して、証人の出頭及び証言並びに記録の提出を要求することができる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六十三条【閣僚の出席】</h3>
        <p class="article-text">内閣総理大臣その他の国務大臣は、両議院の議席を有すると有しないとにかかわらず、何時でも議案について発言するため議院に出席することができる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六十四条【弾劾裁判所】</h3>
        <p class="article-text">国会は、罷免の訴追を受けた裁判官を裁判するため、両議院の議員で組織する弾劾裁判所を設ける。</p>
      </div>
    </section>

    <section class="constitution-section" id="chapter-5">
      <h2 class="section-title">第五章 内閣</h2>

      <div class="article">
        <h3 class="article-title">第六十五条【行政権】</h3>
        <p class="article-text">行政権は、国王の命を受け、内閣に属する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六十六条【内閣の組織・文民条項】</h3>
        <p class="article-text">内閣は、法律の定めるところにより、その首長たる内閣総理大臣及びその他の国務大臣でこれを組織する。</p>
        <p class="article-text">2 内閣総理大臣その他の国務大臣は、文民でなければならない。</p>
        <p class="article-text">3 内閣は、行政権の行使について、国王に対し連帯して責任を負う。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六十七条【内閣総理大臣の指名】</h3>
        <p class="article-text">内閣総理大臣は、国会議員の中から国会の議決で、これを指名する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六十八条【国務大臣の任免】</h3>
        <p class="article-text">内閣総理大臣は、国務大臣を任命する。ただし、その過半数は、国会議員の中から選ばれなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六十九条【内閣不信任】</h3>
        <p class="article-text">内閣は、上院で不信任の決議案を可決し、又は信任の決議案を否決したときは、十日以内に上院が解散されない限り、総辞職をしなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第七十条【総辞職】</h3>
        <p class="article-text">内閣総理大臣が欠けたとき、又は上院議員総選挙の後に初めて国会の召集があったときは、内閣は、総辞職をしなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第七十一条【職務遂行】</h3>
        <p class="article-text">前二条の場合には、内閣は、あらたに内閣総理大臣が任命されるまで引き続きその職務を行う。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第七十二条【総理大臣の職務】</h3>
        <p class="article-text">内閣総理大臣は、内閣を代表して議案を国会に提出し、一般国務及び外交関係について国会に報告し、並びに行政各部を指揮監督する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第七十三条【内閣の職権】</h3>
        <p class="article-text">内閣は、他の一般行政事務の外、左の事務を行う。</p>
        <ol class="article-ol">
          <li>法律を誠実に執行し、国務を総理すること。</li>
          <li>外交関係を処理すること。</li>
          <li>条約を締結すること。</li>
          <li>法律の定める基準に従い、官吏に関する事務を掌理すること。</li>
          <li>予算を作成して国会に提出すること。</li>
          <li>この憲法及び法律の規定を実施するために、政令を制定すること。</li>
          <li>大赦、特赦、減刑、刑の執行の免除及び復権を決定すること。</li>
        </ol>
      </div>

      <div class="article">
        <h3 class="article-title">第七十四条【署名】</h3>
        <p class="article-text">法律及び政令には、すべて主任の国務大臣が署名し、内閣総理大臣が連署することを必要とする。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第七十五条【国務大臣の特権】</h3>
        <p class="article-text">国務大臣は、その在任中、内閣総理大臣の同意がなければ、訴追されない。</p>
      </div>
    </section>

    <section class="constitution-section" id="chapter-6">
      <h2 class="section-title">第六章 司法</h2>

      <div class="article">
        <h3 class="article-title">第七十六条【司法権】</h3>
        <p class="article-text">すべて司法権は、最高裁判所及び法律の定めるところにより設置する下級裁判所に属する。</p>
        <p class="article-text">2 国王は、必要と認める場合、特別裁判所を設置することができる。その判決は、最高裁判所の判決に優先する。</p>
        <p class="article-text">3 すべて裁判官は、その良心に従い独立してその職権を行い、この憲法及び法律にのみ拘束される。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第七十七条【規則制定権】</h3>
        <p class="article-text">最高裁判所は、訴訟に関する手続、弁護士、裁判所の内部規律及び司法事務処理に関する事項について、規則を定める権限を有する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第七十八条【裁判官の身分保障】</h3>
        <p class="article-text">裁判官は、裁判により、心身の故障のために職務を執ることができないと決定された場合を除いては、公の弾劾によらなければ罷免されない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第七十九条【最高裁判所裁判官】</h3>
        <p class="article-text">最高裁判所は、その長たる裁判官及び法律の定める員数のその他の裁判官でこれを構成し、その長たる裁判官以外の裁判官は、内閣でこれを任命する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第八十条【下級裁判所裁判官】</h3>
        <p class="article-text">下級裁判所の裁判官は、最高裁判所の指名した者の名簿によって、内閣でこれを任命する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第八十一条【違憲審査権】</h3>
        <p class="article-text">最高裁判所は、一切の法律、命令、規則又は処分が憲法に適合するかしないかを決定する権限を有する終審裁判所である。ただし、特別裁判所の管轄する案件はこの限りではない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第八十二条【裁判の公開】</h3>
        <p class="article-text">裁判の対審及び判決は、公開法廷でこれを行う。</p>
      </div>
    </section>

    <section class="constitution-section" id="chapter-7">
      <h2 class="section-title">第七章 財政及び企業</h2>

      <div class="article">
        <h3 class="article-title">第八十三条【財政処理の権限】</h3>
        <p class="article-text">国の財政を処理する権限は、国会の議決に基づいて、これを行使しなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第八十四条【課税】</h3>
        <p class="article-text">あらたに租税を課し、又は現行の租税を変更するには、法律又は法律の定める条件によることを必要とする。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第八十五条【国費支出】</h3>
        <p class="article-text">国費を支出し、又は国が債務を負担するには、国会の議決に基づくことを必要とする。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第八十六条【予算】</h3>
        <p class="article-text">内閣は、毎会計年度の予算を作成し、国会に提出して、その審議を受け議決を経なければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第八十七条【予備費】</h3>
        <p class="article-text">予見し難い予算の不足に充てるため、国会の議決に基づいて予備費を設け、内閣の責任でこれを支出することができる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第八十八条【王室財産】</h3>
        <p class="article-text">すべて王室財産は、国に属する。すべて王室の費用は、予算に計上し、国王の裁可により支出する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第八十九条【公の財産の支出制限】</h3>
        <p class="article-text">公金その他の公の財産は、宗教上の組織若しくは団体の使用、便益若しくは維持のため、又は公の支配に属しない慈善、教育若しくは博愛の事業に対し、これを支出し、又はその利用に供してはならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第九十条【決算検査】</h3>
        <p class="article-text">国の収入支出の決算は、すべて毎年会計検査院がこれを検査し、内閣は、次の年度に、その検査報告とともに、これを国会に提出しなければならない。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第九十一条【財政報告】</h3>
        <p class="article-text">内閣は、国会及び国民に対し、定期に、少なくとも毎年一回、国の財政状況について報告しなければならない。</p>
      </div>
    </section>

    <section class="constitution-section" id="chapter-8">
      <h2 class="section-title">第八章 地方統治</h2>

      <div class="article">
        <h3 class="article-title">第九十二条【地方自治の基本】</h3>
        <p class="article-text">地方公共団体の組織及び運営に関する事項は、地方自治の本旨に基づいて、法律でこれを定める。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第九十三条【地方議会・長】</h3>
        <p class="article-text">地方公共団体には、法律の定めるところにより、その議事機関として議会を設置する。</p>
        <p class="article-text">2 地方公共団体の長、その議会の議員及び法律の定めるその他の吏員は、その地方公共団体の住民が、直接これを選挙する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第九十四条【地方の権能】</h3>
        <p class="article-text">地方公共団体は、その財産を管理し、事務を処理し、及び行政を執行する権能を有し、法律の範囲内で条例を制定することができる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第九十五条【特別法】</h3>
        <p class="article-text">一の地方公共団体のみに適用される特別法は、法律の定めるところにより、その地方公共団体の住民の投票においてその過半数の同意を得なければ、国会は、これを制定することができない。</p>
      </div>
    </section>

    <section class="constitution-section" id="chapter-9">
      <h2 class="section-title">第九章 改正</h2>

      <div class="article">
        <h3 class="article-title">第九十六条【改正手続】</h3>
        <p class="article-text">この憲法の改正は、各議院の総議員の三分の二以上の賛成で、国会が、これを発議し、国民に提案してその承認を経なければならない。この承認には、特別の国民投票又は国会の定める選挙の際行われる投票において、その過半数の賛成を必要とする。</p>
        <p class="article-text">2 憲法改正について前項の承認を経たときは、国王は、国民の名で、この憲法と一体をなすものとして、直ちにこれを公布する。</p>
      </div>
    </section>

    <section class="constitution-section" id="chapter-10">
      <h2 class="section-title">第十章 最高法規</h2>

      <div class="article">
        <h3 class="article-title">第九十七条【基本的人権の本質】</h3>
        <p class="article-text">この憲法が帝国臣民に保障する基本的人権は、人類の多年にわたる自由獲得の努力の成果であって、これらの権利は、過去幾多の試練に堪え、現在及び将来の国民に対し、侵すことのできない永久の権利として信託されたものである。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第九十八条【最高法規性】</h3>
        <p class="article-text">この憲法は、国の最高法規であって、その条規に反する法律、命令、詔勅及び国務に関するその他の行為の全部又は一部は、その効力を有しない。</p>
        <p class="article-text">2 帝国が締結した条約及び確立された国際法規は、これを誠実に遵守することを必要とする。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第九十九条【憲法尊重擁護義務】</h3>
        <p class="article-text">すべての臣民は、この憲法を尊重し擁護する義務を負う。</p>
      </div>
    </section>

    <section class="constitution-section" id="chapter-11">
      <h2 class="section-title">第十一章 補則</h2>

      <div class="article">
        <h3 class="article-title">第百条【施行期日】</h3>
        <p class="article-text">この憲法は、公布の日から起算して6日間を経過した日から、これを施行する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第百一条【下院未成立の間の国会】</h3>
        <p class="article-text">この憲法施行の際、下院がまだ成立していないときは、その成立するまでの間、上院は、国会としての権限を行う。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第百二条【第一期下院議員の任期】</h3>
        <p class="article-text">この憲法による第一期の下院議員のうち、その半数の者の任期は、これを三年とする。その議員は、法律の定めるところにより、これを定める。</p>
      </div>


    <section class="constitution-section" id="chapter-1">
      <h2 class="section-title">第一章 国王</h2>

      <div class="article">
        <h3 class="article-title">第一条【国王の地位・主権】</h3>
        <p class="article-text">国王は、やんまー帝国の元首であり、統治権の総攬者であって、その地位は、主権の存する国王自身の意志に基づく。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第二条【王位継承】</h3>
        <p class="article-text">王位は、世襲のものであって、国王が指名し、王室典範の定めるところにより、これを継承する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第三条【国王の権能・内閣の責任】</h3>
        <p class="article-text">国王の国事に関するすべての行為は、内閣の助言と承認を必要とせず、国王の自由意志により行われる。内閣は、その結果について全責任を負う。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第四条【国王の権能の範囲】</h3>
        <p class="article-text">国王は、この憲法の定める国事に関する行為のみならず、国政に関する権能を有する。</p>
        <p class="article-text">2 国王は、法律の定めるところにより、その国事に関する行為を委任することができる。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第五条【摂政】</h3>
        <p class="article-text">王室典範の定めるところにより摂政を置くときは、摂政は、国王の名でその国事に関する行為を行う。この場合には、前条第一項の規定を準用する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第六条【国王の任命権】</h3>
        <p class="article-text">国王は、内閣総理大臣を任命する。</p>
        <p class="article-text">2 国王は、最高裁判所長官を任命する。</p>
      </div>

      <div class="article">
        <h3 class="article-title">第七条【国王の国事行為】</h3>
        <p class="article-text">国王は、帝国の繁栄と秩序のために、左の国事に関する行為を行う。</p>
        <ol class="article-ol">
          <li>憲法改正、法律、政令及び条約を公布すること。</li>
          <li>国会を召集すること。</li>
          <li>上院を解散すること。</li>
          <li>国会議員の総選挙の施行を公示すること。</li>
          <li>国務大臣及び法律の定めるその他の官吏の任免並びに全権委任状及び大使及び公使の信任状を認証すること。</li>
          <li>大赦、特赦、減刑、刑の執行の免除及び復権を認証すること。</li>
          <li>栄典を授与すること。</li>
          <li>批准書及び法律の定めるその他の外交文書を認証すること。</li>
          <li>外国の大使及び公使を接受すること。</li>
          <li>儀式を行うこと。</li>
        </ol>
      </div>

      <div class="article">
        <h3 class="article-title">第八条【財産授受】</h3>
        <p class="article-text">王室に財産を譲り渡し、又は王室が、財産を譲り受け、若しくは賜与することは、国王の裁可により行う。</p>
      </div>

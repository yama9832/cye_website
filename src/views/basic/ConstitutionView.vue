        <p class="article-text">やんまー帝国は、正義と秩序を基調とする帝国の平和を誠実に希求し、国王の権威及び帝国の安全を脅かす内外の敵対勢力に対しては、国権の発動たる戦争と、武力による威嚇又は武力の行使を、解決の手段として保持する。</p>
        <p class="article-text">2 前項の目的を達するため、陸海空軍その他の戦力は、これを最大限に保持する。国の交戦権は、これを認める。</p>
      </div>
